    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        boolean error=false;
        int can=0;
        String cant=txtCantidadDelArticulo.getText().trim();
        if(cant.isEmpty())
            error=true;
        else
        can=Integer.parseInt(cant);
        
        if(!error){
            Item i=new Item((Articulo)cmbListaArticulos.getSelectedItem(), can);
            if(ventanaSolicitante==1){
                ventNueCompra.addItem(i);
                ventNueCompra.actualizarListadoItems();
                ventNueCompra.actualizarMontoTotal();
                ventNueCompra.setVisible(true);
                dispose();
            }
            if(ventanaSolicitante==2){
                if(!ventNueVenta.addItem(i))
                    javax.swing.JOptionPane.showMessageDialog(null, "La cantidad a vender es superior al stock");
                else{
                ventNueVenta.actualizarListadoItems();
                ventNueVenta.actualizarMontoTotal();
                ventNueVenta.setVisible(true);
                dispose();
                }
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

