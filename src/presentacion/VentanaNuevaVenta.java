    private void btnConfirmarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarVentaActionPerformed
        boolean error=false;
        String dni=txtDniComprador.getText().trim();
        if(dni.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Por favor ingrese el DNI del cliente");
            error=true;
        }
        if(presupuesto.getDetallePresupuesto().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Agregue primero los items a comprar");
            error=true;
        }
        
        if(!error){
            LinkedList<Item> copiaDetPres=new LinkedList(presupuesto.getDetallePresupuesto());
            Venta v=new Venta(dni,copiaDetPres);
            comercio.addVenta(v);
            modeloPresupuesto.clear();
            presupuesto.getDetallePresupuesto().clear();
            txtMontoTotal.setText("");
            txtDniComprador.setText("");
            javax.swing.JOptionPane.showMessageDialog(null, "La venta se registro correctamente");
        }
    }//GEN-LAST:event_btnConfirmarVentaActionPerformed

