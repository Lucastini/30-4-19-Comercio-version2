    private void formatoDni(KeyEvent e,JTextField comp){
        char c=e.getKeyChar();
        if(!Character.isDigit(c)&&c!=KeyEvent.VK_BACK_SPACE||comp.getText().trim().length()>=8){
            e.consume();
            getToolkit().beep();
            javax.swing.JOptionPane.showMessageDialog(null, "El DNI debe tener 8 digitos y no puede contener puntos");
        }       
    }

